
package pkg6.condicionalifelse;

public class CondicionalIfElse {

    public static void main(String[] args) {
        
        int num1 = 5;
        int num2 = 10;
        
        if (num1 > num2) {
            System.out.println("El num1 es mayor que el num2");
        } else {
            System.out.println("El num2 es mayor que el num1");
        }
    }
    
}
